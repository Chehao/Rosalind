package rosalind.teman;

public class FASTA {

	
	private String label;
	private String str;
	
	public FASTA(String label, String str){
		this.label = label;
		this.str = str;
	}
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
	public String getStr() {
		return str;
	}
	public void setStr(String str) {
		this.str = str;
	}
	 
}
